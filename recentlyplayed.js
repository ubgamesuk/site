/* ===========================================
   RECENTLY PLAYED MANAGER
   =========================================== */

export function addRecentlyPlayed(game) {

    if (!game || !game.img || !game.url) {
        console.error("addRecentlyPlayed: missing required game fields", game);
        return;
    }
    let recent = JSON.parse(localStorage.getItem("recentlyPlayed")) || [];

    recent = recent.filter(g => g.id !== game.id);

    recent.unshift(game);

    recent = recent.slice(0, 10);

    localStorage.setItem("recentlyPlayed", JSON.stringify(recent));
}



export function loadRecentlyPlayed() {
    const slider = document.getElementById("recentlyPlayedSlider");
    const subtitle = document.querySelector("#recentslidertitle");
    if (!slider) return;

    const recent = JSON.parse(localStorage.getItem("recentlyPlayed")) || [];

    // If no games → hide whole section
    if (recent.length === 0) {
        if (subtitle) subtitle.style.display = "none";
        if (slider) slider.style.display = "none";
        return;
    }

    // If there are games → show section
    if (subtitle) subtitle.style.display = "block";
    if (slider) slider.style.display = "flex";

    slider.innerHTML = "";

    recent.forEach(game => {
        if (!game.img || !game.url) return;

        const card = document.createElement("div");
        card.className = "recentslide";

        card.innerHTML = `
            <img src="${game.img}" alt="">
        `;

        card.addEventListener("click", () => {
            window.location.href = game.url;
        });

        slider.appendChild(card);
    });
}
